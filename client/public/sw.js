const CACHE_NAME = 'controlflow-v1.0.0';
const STATIC_CACHE = 'static-v1.0.0';
const DYNAMIC_CACHE = 'dynamic-v1.0.0';
const API_CACHE = 'api-v1.0.0';

// Arquivos para cache est√°tico
const STATIC_FILES = [
  '/',
  '/index.html',
  '/manifest.json',
  '/favicon.ico',
  '/android-chrome-192x192.png',
  '/android-chrome-512x512.png',
  '/apple-touch-icon.png',
  '/favicon-16x16.png',
  '/favicon-32x32.png',
  '/site.webmanifest'
];

// Estrat√©gias de cache
const CACHE_STRATEGIES = {
  STATIC_FIRST: 'cache-first',
  NETWORK_FIRST: 'network-first',
  STALE_WHILE_REVALIDATE: 'stale-while-revalidate',
  CACHE_ONLY: 'cache-only',
  NETWORK_ONLY: 'network-only'
};

// Configura√ß√µes de cache
const CACHE_CONFIG = {
  STATIC: {
    strategy: CACHE_STRATEGIES.STATIC_FIRST,
    maxAge: 30 * 24 * 60 * 60 * 1000, // 30 dias
    maxEntries: 100
  },
  DYNAMIC: {
    strategy: CACHE_STRATEGIES.NETWORK_FIRST,
    maxAge: 24 * 60 * 60 * 1000, // 24 horas
    maxEntries: 50
  },
  API: {
    strategy: CACHE_STRATEGIES.STALE_WHILE_REVALIDATE,
    maxAge: 5 * 60 * 1000, // 5 minutos
    maxEntries: 20
  }
};

// Instala√ß√£o do Service Worker
self.addEventListener('install', (event) => {
  console.log('üîÑ Service Worker instalando...');
  
  event.waitUntil(
    caches.open(STATIC_CACHE)
      .then((cache) => {
        console.log('üì¶ Cache est√°tico aberto');
        return cache.addAll(STATIC_FILES);
      })
      .then(() => {
        console.log('‚úÖ Cache est√°tico preenchido');
        return self.skipWaiting();
      })
      .catch((error) => {
        console.error('‚ùå Erro ao instalar cache est√°tico:', error);
      })
  );
});

// Ativa√ß√£o do Service Worker
self.addEventListener('activate', (event) => {
  console.log('üöÄ Service Worker ativando...');
  
  event.waitUntil(
    Promise.all([
      // Limpar caches antigos
      caches.keys().then((cacheNames) => {
        return Promise.all(
          cacheNames.map((cacheName) => {
            if (cacheName !== STATIC_CACHE && 
                cacheName !== DYNAMIC_CACHE && 
                cacheName !== API_CACHE) {
              console.log('üóëÔ∏è Removendo cache antigo:', cacheName);
              return caches.delete(cacheName);
            }
          })
        );
      }),
      // Tomar controle imediatamente
      self.clients.claim()
    ]).then(() => {
      console.log('‚úÖ Service Worker ativado e pronto');
    })
  );
});

// Intercepta√ß√£o de requisi√ß√µes
self.addEventListener('fetch', (event) => {
  const { request } = event;
  const url = new URL(request.url);
  
  // Pular requisi√ß√µes n√£o-GET
  if (request.method !== 'GET') {
    return;
  }
  
  // Pular requisi√ß√µes de extens√µes do navegador
  if (url.protocol === 'chrome-extension:') {
    return;
  }
  
  // Estrat√©gias baseadas no tipo de recurso
  if (isStaticAsset(url)) {
    event.respondWith(handleStaticAsset(request));
  } else if (isApiRequest(url)) {
    event.respondWith(handleApiRequest(request));
  } else if (isNavigationRequest(request)) {
    event.respondWith(handleNavigationRequest(request));
  } else {
    event.respondWith(handleDynamicRequest(request));
  }
});

// Verificar se √© um asset est√°tico
function isStaticAsset(url) {
  const staticExtensions = ['.js', '.css', '.png', '.jpg', '.jpeg', '.gif', '.svg', '.ico', '.woff', '.woff2', '.ttf', '.eot'];
  return staticExtensions.some(ext => url.pathname.includes(ext)) || 
         STATIC_FILES.includes(url.pathname);
}

// Verificar se √© uma requisi√ß√£o de API
function isApiRequest(url) {
  return url.pathname.startsWith('/api/') || 
         url.hostname.includes('supabase') ||
         url.hostname.includes('api.');
}

// Verificar se √© uma requisi√ß√£o de navega√ß√£o
function isNavigationRequest(request) {
  return request.mode === 'navigate' || 
         (request.method === 'GET' && request.headers.get('accept').includes('text/html'));
}

// Estrat√©gia Cache First para assets est√°ticos
async function handleStaticAsset(request) {
  try {
    const cache = await caches.open(STATIC_CACHE);
    const cachedResponse = await cache.match(request);
    
    if (cachedResponse) {
      console.log('üì¶ Asset est√°tico servido do cache:', request.url);
      return cachedResponse;
    }
    
    const networkResponse = await fetch(request);
    
    if (networkResponse.ok) {
      cache.put(request, networkResponse.clone());
      console.log('üì¶ Asset est√°tico adicionado ao cache:', request.url);
    }
    
    return networkResponse;
  } catch (error) {
    console.error('‚ùå Erro ao buscar asset est√°tico:', error);
    return new Response('Asset n√£o encontrado', { status: 404 });
  }
}

// Estrat√©gia Network First para APIs
async function handleApiRequest(request) {
  const cache = await caches.open(API_CACHE);
  
  try {
    // Tentar rede primeiro
    const networkResponse = await fetch(request);
    
    if (networkResponse.ok) {
      // Cache da resposta
      cache.put(request, networkResponse.clone());
      console.log('üåê API servida da rede e cacheada:', request.url);
      return networkResponse;
    }
    
    throw new Error('Rede falhou');
  } catch (error) {
    // Fallback para cache
    const cachedResponse = await cache.match(request);
    
    if (cachedResponse) {
      console.log('üì¶ API servida do cache (fallback):', request.url);
      return cachedResponse;
    }
    
    // Fallback offline
    return new Response(JSON.stringify({
      error: 'Servi√ßo indispon√≠vel',
      message: 'Voc√™ est√° offline e n√£o h√° dados em cache'
    }), {
      status: 503,
      headers: { 'Content-Type': 'application/json' }
    });
  }
}

// Estrat√©gia Stale While Revalidate para navega√ß√£o
async function handleNavigationRequest(request) {
  const cache = await caches.open(DYNAMIC_CACHE);
  
  try {
    // Buscar do cache primeiro
    const cachedResponse = await cache.match(request);
    
    // Buscar da rede em background
    const networkPromise = fetch(request).then(async (response) => {
      if (response.ok) {
        cache.put(request, response.clone());
        console.log('üîÑ P√°gina atualizada em background:', request.url);
      }
      return response;
    }).catch(() => null);
    
    // Retornar cache se dispon√≠vel, sen√£o aguardar rede
    if (cachedResponse) {
      console.log('üì¶ P√°gina servida do cache:', request.url);
      return cachedResponse;
    }
    
    const networkResponse = await networkPromise;
    if (networkResponse) {
      return networkResponse;
    }
    
    // Fallback offline
    return new Response(`
      <!DOCTYPE html>
      <html>
        <head>
          <title>Offline - ControlFlow</title>
          <meta charset="utf-8">
          <meta name="viewport" content="width=device-width, initial-scale=1">
          <style>
            body { 
              font-family: Arial, sans-serif; 
              text-align: center; 
              padding: 50px; 
              background: #f5f5f5; 
            }
            .offline-card {
              background: white;
              padding: 40px;
              border-radius: 10px;
              box-shadow: 0 4px 6px rgba(0,0,0,0.1);
              max-width: 500px;
              margin: 0 auto;
            }
            .offline-icon {
              font-size: 48px;
              margin-bottom: 20px;
            }
          </style>
        </head>
        <body>
          <div class="offline-card">
            <div class="offline-icon">üì±</div>
            <h1>Voc√™ est√° offline</h1>
            <p>Algumas funcionalidades podem n√£o estar dispon√≠veis.</p>
            <p>Tente verificar sua conex√£o com a internet.</p>
            <button onclick="window.location.reload()">Tentar novamente</button>
          </div>
        </body>
      </html>
    `, {
      headers: { 'Content-Type': 'text/html' }
    });
  } catch (error) {
    console.error('‚ùå Erro na navega√ß√£o:', error);
    return new Response('Erro interno', { status: 500 });
  }
}

// Estrat√©gia Network First para requisi√ß√µes din√¢micas
async function handleDynamicRequest(request) {
  const cache = await caches.open(DYNAMIC_CACHE);
  
  try {
    const networkResponse = await fetch(request);
    
    if (networkResponse.ok) {
      cache.put(request, networkResponse.clone());
      console.log('üåê Recurso din√¢mico servido da rede:', request.url);
      return networkResponse;
    }
    
    throw new Error('Rede falhou');
  } catch (error) {
    const cachedResponse = await cache.match(request);
    
    if (cachedResponse) {
      console.log('üì¶ Recurso din√¢mico servido do cache:', request.url);
      return cachedResponse;
    }
    
    return new Response('Recurso n√£o encontrado', { status: 404 });
  }
}

// Limpeza peri√≥dica de cache
async function cleanupOldCaches() {
  const cacheNames = await caches.keys();
  
  for (const cacheName of cacheNames) {
    const cache = await caches.open(cacheName);
    const requests = await cache.keys();
    
    for (const request of requests) {
      const response = await cache.match(request);
      if (response) {
        const cacheTime = response.headers.get('sw-cache-time');
        if (cacheTime) {
          const age = Date.now() - parseInt(cacheTime);
          const maxAge = CACHE_CONFIG[cacheName]?.maxAge || 24 * 60 * 60 * 1000;
          
          if (age > maxAge) {
            await cache.delete(request);
            console.log('üóëÔ∏è Cache expirado removido:', request.url);
          }
        }
      }
    }
  }
}

// Limpeza de cache a cada hora
setInterval(cleanupOldCaches, 60 * 60 * 1000);

// Mensagens do Service Worker
self.addEventListener('message', (event) => {
  if (event.data && event.data.type === 'SKIP_WAITING') {
    self.skipWaiting();
  }
  
  if (event.data && event.data.type === 'CLEAR_CACHE') {
    event.waitUntil(
      caches.keys().then((cacheNames) => {
        return Promise.all(
          cacheNames.map((cacheName) => caches.delete(cacheName))
        );
      })
    );
  }
});

